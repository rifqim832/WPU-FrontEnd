var angkot = 1;
var angkotberoprasi = 6;
var jumlahAngkot = 10;
for(var angkot; angkot  <= jumlahAngkot; angkot++){
    if(angkot <= angkotberoprasi){
        console.log("Angkot NO." + angkot + " beroprasi dengan baik");
    }else{
        console.log("Angkot NO." + angkot + " sedang tidak beroprasi");
    }
    
}
